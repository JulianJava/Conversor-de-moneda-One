package conversorDeMonedas;

public class Moneda {
  
	public double pesoDolar (double valor){
		return valor*0.037;
	}
    public double dolarPeso(double valor) {
    	return valor*271.62;
    }
    public double pesoEuro(double valor) {
    	return valor*0.0033;
    }
    public double euroPeso(double valor) {
    	return valor*300.21;
    }
    public double pesoLibra(double valor) {
    	return valor*0.0029;
    }
    public double libraPeso(double valor) {
    	return valor*350.44;
    }
    public double pesoYen(double valor) {
    	return valor*0.52;
    }
    public double yenPeso(double valor) {
    	return valor*1.93;
    }
    public double pesoWon(double valor) {
    	return valor*4.70;
    }
    public double wonPeso(double valor) {
    	return valor*0.21;
    }
    
    
}
